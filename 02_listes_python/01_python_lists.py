# Étape 1 : Création d'une liste
# Ici, on crée une liste appelée 'fam' qui contient :
# - "Yann" et sa taille 1.73
# - "Marie" et sa taille 1.68
# - "Paul" et sa taille 1.82
fam = ["Yann", 1.82,"Marie", 1.68, "Paul", 1.73]

#  tape 2 : Vérifier combien d’éléments il y a dans la liste
print(len(fam))
#  ésultat attendu : 6  (3 prénoms + 3 tailles)

#  Étape 3 : Vérifier de quel type est cette variable
print(type(fam))
#  ésultat attendu : <class 'list'>


# On affiche le contenu de la liste
print(fam)          
#  Résultat attendu : ["Yann", 1.82,"Marie", 1.68, "Paul", 1.73]